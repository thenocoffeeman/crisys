              <h2>Welcome</h2>
                
                <p>The transition from paper-based health records to electronic health records (EHR) is underway.  There are many systems marketed to medical providers, and some systems oriented towards consumers, such as Google Health and Microsoft HealthVault.<br /><br />
A comprehensive EHR system would provide interfaces for many kinds of organizations (doctor's offices, hospitals, pharmacies, insurance companies, etc.) and many categories of users within each organization.  In this project, we limit the scope to patients and their agents (e.g., spouse or parents) and clinicians (e.g.,doctors and nurses).  Also, the information maintained by the system is limited primarily to EHRs and information needed to control access to EHRs.  Work schedules, appointments, billing, insurance, etc., are omitted.
</p>

